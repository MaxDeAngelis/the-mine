using UnityEngine;
using System.Collections;
using UnityEditor;

[CustomEditor(typeof(ShaftNode))]
public class IShaftNode : INode {
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/// 								     	     	PRIVATE VARIABLES   			     					     ///
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	/// 								     		PRIVATE FUNCTIONS											     ///
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	public override void OnInspectorGUI() {
		GUI.changed = false;

		// Get a reference to the extended class
		ShaftNode _node = target as ShaftNode;

		_node.type = NODE_TYPE.Shaft;

		// If changed then you need to set dirty
		if (GUI.changed) {
			EditorUtility.SetDirty(_node);
		}
	}
}
